#include "3-calc.h"

/**
 * op_add - sum function
 * @a: first number
 * @b: second number
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction function
 * @a: first number
 * @b: second number
 *
 * Return: the substraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication function
 * @a: first number
 * @b: second number
 *
 * Return: The multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: first number
 * @b: second number
 *
 * Return: The division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module function
 * @a: first number
 * @b: second number
 *
 * Return: the module
 */
int op_mod(int a, int b)
{
	return (a % b);
}
